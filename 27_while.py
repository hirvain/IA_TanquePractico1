#bucle while que se ejecute hasta que x valga 15 con incrementos de 5.
x = 0
while x <= 15:
	print(x)
	x += 5
   
#bucle while que se ejecute hasta que x valga -100 con decrementos de 20.
x = 0
while x >= -100:
	print(x)
	x -= 20
    
# bucle while que se ejecute hasta que x valga 0 con decrementos de 1
x = 10
while x >= 0:
	print('El valor de x es: ', x)
	x -= 1